// ============================================
// HELPER FUNCTIONS
// ============================================

/**
 * Get all branches from all stores
 */
export const getAllBranches = (): Branch[] => {
  return stores.flatMap(store => store.branches);
};

/**
 * Get branches by city
 */
export const getBranchesByCity = (city: string): Branch[] => {
  return getAllBranches().filter(branch => branch.city === city);
};

/**
 * Get branches by store ID
 */
export const getBranchesByStore = (storeId: string): Branch[] => {
  const store = stores.find(s => s.id === storeId);
  return store ? store.branches : [];
};

/**
 * Get store by ID
 */
export const getStoreById = (storeId: string): Store | undefined => {
  return stores.find(s => s.id === storeId);
};

/**
 * Get all hypermarkets/supermarkets (exclude electronics stores)
 */
export const getSupermarkets = (): Store[] => {
  const electronicsStoreIds = ['oscar', 'btech', 'extra', 'noon'];
  return stores.filter(s => !electronicsStoreIds.includes(s.id));
};

/**
 * Get all electronics stores
 */
export const getElectronicsStores = (): Store[] => {
  const electronicsStoreIds = ['oscar', 'btech', 'extra', 'noon'];
  return stores.filter(s => electronicsStoreIds.includes(s.id));
};

/**
 * Search stores by name (Arabic or English)
 */
export const searchStores = (query: string): Store[] => {
  const lowerQuery = query.toLowerCase();
  return stores.filter(
    s =>
      s.nameAr.toLowerCase().includes(lowerQuery) ||
      s.nameEn.toLowerCase().includes(lowerQuery)
  );
};

export default stores;